namespace JSONParserTool.Lexer;

public class JsonDictionary
{
    public const char BeginArray = (char)0x5b;

    public const char EndArray = (char)0x5d;

    public const char BeginObject = (char)0x7b;

    public const char EndObject = (char)0x7d;

    public const char NameSeparator = (char)0x3a;

